#Aritmeticos
print(4 + 2)
print(4 - 2)
print(4 * 2)
print(4 / 2)
print(4 ** 2)

# Para cadenas
print("Hola " + str(3)) #Lo agrega al lado derecho
print("Hola" * 4) #Lo repite el numero de veces que se diga

#Comparativos
print(4 < 2)
print(4 > 2)
print(4 <= 2)
print(4 >= 2)
print(4 == 2)
print(4 != 2)

#Logicos
print(1 == 1 and 2==2)
print(1 == 2 or 2==1)
print(not(1==2))